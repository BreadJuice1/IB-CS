


 class student {
	long studentnumber;
	int grade;
	String lastname;
	String firstname;
	String gender;


	student(int studentnumber, int grade, String lastname, String firstname, String gender) {
		this.studentnumber = studentnumber;
		this.grade         = grade;
		this.lastname      = lastname;
		this.firstname     = firstname;
		this.gender        = gender;
	}//constructor for student

}//student
